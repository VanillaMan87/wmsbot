void shooterMotors(int speed) {
	motor[stl] = speed;
	motor[sbl] = speed;
	motor[str] = speed;
	motor[sbr] = speed;
}

void intakeMotors(int speed) {
	motor[intakefront] = speed;
	motor[intakeback] = speed;
}


task main()	{


	//////////////////////////////////////////////////////////
	//
	// CONFIGURATION
	//


	int shooterSpeed = 127;
	int autoStart = 1;



	while (true) {

		// Run this until autoStart is 0
		if (autoStart == 1) {
			shooterMotors(shooterSpeed);
			intakeMotors(127);

			// Exit AutoStart Mode when button is pressed
			// Turn off Elevator
			// Leave shooters on

			if (vexRT[Btn8U] == 1) {
				intakeMotors(0);
				autoStart = 0;
			}
		}


		// Used to filter out joystick abberations
		int threshold = 10;

		//////////////////////////////////////////////////////////
		//
		// DRIVE
		//
		// This code has filtering in place
		// Because the joystick returns a value that is not always
		// exactly zero.

		if(abs(vexRT[Ch3]) > threshold)
		{
			motor[dl]  = (vexRT[Ch3] );
		}
		else
		{
			motor[dl]  = 0;
		}

		if(abs(vexRT[Ch2]) > threshold)
		{
			motor[dr] = (vexRT[Ch2]);
		}
		else
		{
			motor[dr] = 0;
		}

	//////////////////////////////////////////////////////////
	//
	// SHOOTERS
	//

		if (vexRT[Btn6U] == 1) {
			shooterMotors(shooterSpeed);
			} else {
			shooterMotors(0);
		}

	//////////////////////////////////////////////////////////
	//
	// INTAKE MECHANISM
	//

		if (vexRT[Btn5U] == 1) {
			intakeMotors(127);
			} else {

			// If 5U isn't pressed
			// check for 5D which is
			// reverse. If neither, turn off.

			if (vexRT[Btn5D] == 1) {
				intakeMotors(-127);
				} else {

				// No buttons pressed? Stop motors
				intakeMotors(0);
			}
		}
	}
}
